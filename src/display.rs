use colored::Colorize;
use std::fmt::Display;

pub fn banner() -> impl Display {
    let mut x = "-".green().to_string();
    for _ in 0..12 {
        x.push_str("-".red().to_string().as_str());
        x.push_str("-".green().to_string().as_str());
    }

    x.to_string()
}

///               (#((##%%%%%((#            
///           #((#%%%%#(((#%%%#(/((        
///        %(#%%&&&&%#&&&%%%%%%%#####      
/// ****** %&     &&&&&&%%%%#((((((((#,    
/// ********     (&%%%%%&&&&%%%%%##(((#(   
///  *****       &%%%%%%%%%%%%%%%%######*  
///           ****************************
///          ******************************
///          ******************************
pub fn santa_hat() -> impl Display {
    let mut hat = "           (#((##%%%%%((#
        #((#%%%%#(((#%%%#(/((
     %(#%%&&&&%#&&&%%%%%%%#####"
        .red()
        .to_string();
    hat.push('\n');
    hat.push_str(" ******".bright_white().to_string().as_str());
    hat.push_str(" %&     &&&&&&%%%%#((((((((#,".red().to_string().as_str());
    hat.push('\n');
    hat.push_str(" ********".bright_white().to_string().as_str());
    hat.push_str("     (&%%%%%&&&&%%%%%##(((#(".red().to_string().as_str());
    hat.push('\n');
    hat.push_str("  *****".bright_white().to_string().as_str());
    hat.push_str("       &%%%%%%%%%%%%%%%%######*".red().to_string().as_str());
    hat.push('\n');
    hat.push_str(
        "           **************************** 
          ******************************
          ******************************"
            .bright_white()
            .to_string()
            .as_str(),
    );
    hat
}

